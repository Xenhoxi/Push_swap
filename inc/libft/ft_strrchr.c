#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int	len;

	len = ft_strlen(s);
	while (len >= 0)
	{
		if (s[len] == (char)c)
			return (s += len);
		len--;
	}
	return (0);
}
